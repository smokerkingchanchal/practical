// USE ELSE IF LADDER. 
package JavaProgram;

public class elseifLadder {

	public static void main(String[] args) {
		String name ="pritam";
		
		if(name == "riya")
		{
			System.out.println("name is riya");
		}
		else if (name=="pritam"){
			System.out.println("name is pritam");
		}
		else if(name=="rohit")
		{
			System.out.println("name is rohit");
		}else
		{
			System.out.println(name);
		}
	
		

	}

}